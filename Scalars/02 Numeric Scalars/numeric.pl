#!/usr/bin/perl

$integer = 100;
$negative = -200;
$float = 100.150;
$bigfloat = -1.2E-23;

# 377 octal, also 255 decimal
$octal = 0377;

# FF hex, also 255 decimal
$hex = 0xff;


print "integer = $integer\n";
print "negative = $negative\n";
print "floating = $float\n";
print "bigfloat = $bigfloat\n";
print "octal = $octal\n";
print "hexadecimal = $hex\n";