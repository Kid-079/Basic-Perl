#!/usr/bin/perl

print "File name   : " . __FILE__ . "\n";
print "Line Number : " . __LINE__ . "\n";
print "Package     : " . __PACKAGE__ . "\n";

# Cannot be interpolated

print "\n";
print "Ini adalah Tiga Jenis Special Literals :\n";

print "__FILE__ __LINE__ __PACKAGE__\n";



# CATATAN :

# Ini adalah Tiga macam Special Literals :
# 1. __FILE__    ==> Filename

# 2. __LINE__    ==> Line number

# 3. __PACKAGE__ ==> Package name at that point in your program

